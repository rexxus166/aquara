function getArtikelById($id) {
    $artikel = getAllArtikel();
    return isset($artikel[$id]) ? $artikel[$id] : null;
}

function getArtikelTerkait($currentId, $limit = 3) {
    $artikel = getAllArtikel();
    unset($artikel[$currentId]);
    return array_slice($artikel, 0, $limit, true);
}
